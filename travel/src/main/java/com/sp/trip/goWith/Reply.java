package com.sp.trip.goWith;

public class Reply {
	private int goWithReplyNum;
	private int goWithNum;
	private String userId;
	private String content;
	private String reg_date;
	
	public int getGoWithReplyNum() {
		return goWithReplyNum;
	}
	public void setGoWithReplyNum(int goWithReplyNum) {
		this.goWithReplyNum = goWithReplyNum;
	}
	public int getGoWithNum() {
		return goWithNum;
	}
	public void setGoWithNum(int goWithNum) {
		this.goWithNum = goWithNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
	
}
