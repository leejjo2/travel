package com.sp.trip.partner;

public class Partner {
	private int partnerNum; // 파트너 번호
	private String partnerId; // 파트너 아이디
	private String partnerTradeName; // 파트너 상호명
	private String partnerName; // 파트너 이름
	private String partnerTel; // 파트너 전화번호
	private String tel1;
	private String tel2;
	private String tel3;
	private String partnerInfo; // 파트너 소개
	private String partnerStatus; // 파트너 승인 여부
	private String requestDate; // 요청일
	
	public int getPartnerNum() {
		return partnerNum;
	}
	public void setPartnerNum(int partnerNum) {
		this.partnerNum = partnerNum;
	}
	public String getPartnerId() {
		return partnerId;
	}
	public void setPartnerId(String partnerId) {
		this.partnerId = partnerId;
	}
	public String getPartnerTradeName() {
		return partnerTradeName;
	}
	public void setPartnerTradeName(String partnerTradeName) {
		this.partnerTradeName = partnerTradeName;
	}
	public String getPartnerName() {
		return partnerName;
	}
	public void setPartnerName(String partnerName) {
		this.partnerName = partnerName;
	}
	public String getPartnerTel() {
		return partnerTel;
	}
	public void setPartnerTel(String partnerTel) {
		this.partnerTel = partnerTel;
	}
	public String getTel1() {
		return tel1;
	}
	public void setTel1(String tel1) {
		this.tel1 = tel1;
	}
	public String getTel2() {
		return tel2;
	}
	public void setTel2(String tel2) {
		this.tel2 = tel2;
	}
	public String getTel3() {
		return tel3;
	}
	public void setTel3(String tel3) {
		this.tel3 = tel3;
	}
	public String getPartnerInfo() {
		return partnerInfo;
	}
	public void setPartnerInfo(String partnerInfo) {
		this.partnerInfo = partnerInfo;
	}
	public String getPartnerStatus() {
		return partnerStatus;
	}
	public void setPartnerStatus(String partnerStatus) {
		this.partnerStatus = partnerStatus;
	}
	public String getRequestDate() {
		return requestDate;
	}
	public void setRequestDate(String requestDate) {
		this.requestDate = requestDate;
	}
	
}
